
public class Imovel {
	
	private String imovel;
	//ATENÇÃO: Não pode usar caracteres de acentuação em Java (ç, ´, `, ^, ~, etc...)
	private int preço;
	
	
	
	public String getImovel() {
		return imovel;
	}
	public void setImovel(String imovel) {
		this.imovel = imovel;
	}
	public int getPreço() {
		return preço;
	}
	public void setPreço(int preço) {
		this.preço = preço;
	}
	
	

}
